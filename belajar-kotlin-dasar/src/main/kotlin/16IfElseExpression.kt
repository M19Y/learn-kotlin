fun main() {

    println("\n==== If")
    if(true){
        println("If executed")
    }

    println("\n==== Else")
    if(false){
        println("If executed")
    }else{
        println("Else executed")
    }

    println("\n==== Else If")
    if(false){
        println("If executed")
    }else if(true){
        println("Else If executed")
    }else{
        println("Else executed")
    }
}