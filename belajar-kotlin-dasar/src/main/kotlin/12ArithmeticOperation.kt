fun main() {
    val first = 10 / 3
    println(first) // 3

    val second = 10 + 10 / 2;
    println(second) // 15

    val third: Double = 10.0 / 3.0
    println(third)

    // assignment operation
    var total = 0;

    val product1 = 100
    total = total + product1;

    val product2 = 200
    total = total + product2;

    // augmented assignment
    val product3 = 300
    total += product3;

    println(total)


}